// types

// components
import "@panda-wbc/panda-icon";

// styles
import { styles } from "./styles/styles";

// utils

export class PandaChip extends HTMLElement {
	// css styles
	static get styles() {
		return styles;
	}

	label: string = "";
	
	icon!: string;
	
	disabled: boolean = false;
	
	busy: boolean = false;

	// state props

	// ================================================================================================================
	// LIFE CYCLE =====================================================================================================
	// ================================================================================================================


	// ================================================================================================================
	// RENDERERS ======================================================================================================
	// ================================================================================================================

	render() {
		let iconHtml = ``;

		if (this.icon) {
			iconHtml = `<panda-icon .icon="${this.icon}"></panda-icon>`;
		}

		return `
			<div
				class="chip"
				part="chip"
			>
				${iconHtml}
				${this.label ?? ""}
			</div>
		`;
	}

	// ================================================================================================================
	// HELPERS ========================================================================================================
	// ================================================================================================================

	// ================================================================================================================
	// API ============================================================================================================
	// ================================================================================================================

	// ================================================================================================================
	// EVENTS =========================================================================================================
	// ================================================================================================================
}